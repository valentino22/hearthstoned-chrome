var table = "";
$.each(hearthstoneCards, function(i, value) {

	/* CLASS */
	var classs = "neutral";
	if (!(typeof value.classs === 'undefined')){
		switch(value.classs)
		{
		case 1:
		  classs = "warrior";
		  break;
		case 2:
		  classs = "paladin";
		  break;
		case 3:
		  classs = "hunter";
		  break;
		case 4:
		  classs = "rogue";
		  break;
		case 5:
		  classs = "priest";
		  break;
		case 6:
		  classs = "neutral";
		  break;
		case 7:
		  classs = "shaman";
		  break;
		case 8:
		  classs = "mage";
		  break;
		case 9:
		  classs = "warlock";
		  break;
		case 10:
		  classs = "neutral";
		  break;
		case 11:
		  classs = "druid";
		  break;
		default:
		  classs = "neutral";
		}
	}
	
	/* CARD QUALITY */
	var quality = "";
	if (!(typeof value.quality === 'undefined')){
		switch(value.quality)
		{
		case 0:
		  quality = "basic";
		  break;
		case 1:
		  quality = "common";
		  break;
		case 3:
		  quality = "rare";
		  break;
		case 4:
		  quality = "epic";
		  break;
		case 5:
		  quality = "legendary";
		  break;
		default:
		  quality = "common";
		}
	}

	/* HEALTH vs DURABILITY */
	var health = "";
	var healthType = "";
	var attack = "";
	var attackType = "";
	if (typeof value.health === 'undefined'){
		if (!(typeof value.durability === 'undefined')){
			health = value.durability;
			healthType = "durability";
			attackType = "damage";
			
		}
		else
		{
			health = "";
		}
	}
	else {
		health = value.health;
		healthType = "health";
		attackType = "attack";		
	}
	
	if (!(typeof value.attack === 'undefined')){
		attack = value.attack;
	} else {
		attack = "";
	}
	
	var arenaRank  = "";
	if (!(typeof value.arenaRank === 'undefined')){
		arenaRank = value.arenaRank;
	} 
	
	var description = "";
	if (!(typeof value.description === 'undefined')){
		description = value.description;
	} 

	table += '<tr><td><a href="http://wowhead.com/hearthstone/card='+value.id+'&image" class="quality '+ quality + '" target="_blank">' + value.name + '</a></td>';
	table += '<td class="txclass ' + classs + '">' + classs + '</td>';
	table += '<td><span class="mana">' + value.cost + '</span></td>';
	table += '<td><span class="' + attackType + '">' + attack + '</span></td>';
	table += '<td><span class="' + healthType + '">' + health + '</span></td>';
	table += '<td><span class="arenaRank">' + arenaRank + '</span></td>';
	table += '<td class="description">' + description + '</td>';
	table += '</tr>';
});
$('table').remove();
